#  ESCAPE CHARACTERS 

# Quotes
print('It\'s Python')                    
print("He said, \"Learn Python\"")        

# New line & Tab
print("Hello\nWorld")                     
print("Name:\tSunil")                     

# Backslash
print("This is a backslash: \\")          

# Backspace (removes previous char visually)
print("Helloo\b")                         

# Carriage return (overwrites from start)
print("Hello\rWorld")                     

# Form feed & Vertical tab
print("Hello\fWorld")                     
print("Hello\vWorld")                     

# Unicode character
print("\u2764")                           

# Hexadecimal value
print("\x48\x69")                         

# Octal value
print("\110\151")                         

# Raw string (ignores escape characters)
print(r"C:\new\folder\test")              

# Double quotes inside single quotes (no escape needed)
print('We are students of "University of Hyderabad"')

# Escaping double quotes inside double quotes
print("We are students of \"University of Hyderabad\"")

# Showing escape characters literally
print('\\"University\\"')                 



